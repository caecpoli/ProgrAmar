int MENU()//BASICAMENTE A MOVIAMENTAÇÃO DAS SETAS
{
    char cursor[4] = { Seta, Seta, Seta, Seta };
    int n = 0;
    clear();
    while (cursor[n] != Enter)
    {
        outdoor_M();
        ajustar_linha();

        ajustar_coluna_M();
        printf("%c GAME\n", (n==0)? cursor[n] : ' ');
        ajustar_coluna_M();
        printf("%c OPTIONS\n", (n==1)? cursor[n] : ' ');
        ajustar_coluna_M();
        printf("%c RANKING\n", (n==2)? cursor[n] : ' ');
        ajustar_coluna_M();
        printf("%c EXIT\n", (n==3)? cursor[n] : ' ');

        cursor[n] = getch();

        switch(cursor[n])
        {
             case 'w':
             case 'W':
                    cursor[n] = Seta;
                    n--;
                    break;
             case 's':
             case 'S':
                    cursor[n] = Seta;
                    n++;
                    break;
             default:
                {
                    if (cursor[n] != Enter)
                    {
                        cursor[n] = Seta;
                        n--;
                    }
                }
        }

        if (n<0) n=3;
        else if (n>3) n=0;

        clear();
    }

    return n;
}

