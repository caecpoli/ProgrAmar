int Desistir(int Qual_nvl)
{
    clear();
    char key[2]= { Seta, Seta };
    int i=0;

    while(key[i]!= Enter)
    {
        clear();
        outdoor_M();
        ajustar_linha();
        ajustar_coluna_OP();
        bordas_OP_Top();
        ajustar_coluna_OP();
        printf("%c     VOCE DESEJA PARAR AQUI?        %c\n",186,186);
        ajustar_coluna_OP();
        printf("%c                                    %c\n",186,186);
        ajustar_coluna_OP();
        printf("%c              %c YES                 %c\n",186,((i==0)? key[i]: 32),186);
        ajustar_coluna_OP();
        printf("%c              %c NO                  %c\n",186,((i==1)? key[i]: 32),186);
        ajustar_coluna_OP();
        bordas_OP_Bot();

        key[i]=getch();

        switch(key[i])
        {
        case 'w':
        case 'W':
            key[i]=Seta;
            i--;
            break;
        case 's':
        case 'S':
            key[i]=Seta;
            i++;
            break;
        default:
        {
            if(key[i]!= Enter)
            {
                if(key[i]==ESC) return 0;
                else
                {
                    key[i]=Seta;
                    i--;
                }
            }
        }
        }
        if (i<0) i=1;
        else if (i>1) i=0;
    }

    switch(Qual_nvl)
    {
    case 1:
        if(i==1)
        {
            aux_3++;
            Meta=+800+score;//objetivo para passar de nvl
            Passar_nvl_Facil(score);
        }
        else
            Fim();
        break;

    case 2:
        if(i==1)
        {
            aux_3++;
            Meta=+1500+score;
            Passar_nvl_Facil(score);
        }
        else
            Fim();
        break;

    case 3:
        if(i==1)
        {
            aux_3++;
            Meta=+2200+score;
            Passar_nvl_Med(score);
        }
        else
            Fim();
        break;
    case 4:
        if(i==1)
        {
            aux_3++;
            Meta=+4400+score;
            Passar_nvl_Med(score);
        }
        else
            Fim();
        break;
    case 5:
        if(i==1)
        {
            aux_3++;
            Meta=+4200+score;
            Passar_nvl_Hard(score);
        }
        else
            Fim();
        break;
    case 6:
        if(i==1)
        {
            aux_3++;
            Meta=+12300+score;
            Passar_nvl_Hard(score);
        }
        else
            Fim();
        break;
    }
}
