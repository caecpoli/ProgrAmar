void randomizar(char m, char n, char x, char y)
{
    int valor;

    srand((unsigned)time(NULL));	// parâmetro de aleatoriedade (srand)
    for (i=m; i<=x; i++)//Matriz aleatoria
    {
        for (j=n; j<=y; j++)
        {
            valor = 1 + (rand() % 100);
            if (1<=valor && valor<=20)
            {
                Todos_Maps [i][j] = 'A';
            }
            else if (20<valor && valor<=40)
            {
                Todos_Maps [i][j] = 'B';
            }
            else if (40<valor && valor<=60)
            {
                Todos_Maps [i][j] = 'C';
            }
            else if (60<valor && valor<=80)
            {
                Todos_Maps [i][j] = 'D';
            }
            else
            {
                Todos_Maps [i][j] = 'E';
            }
        }
    }
}

